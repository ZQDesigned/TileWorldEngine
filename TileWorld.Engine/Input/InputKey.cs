namespace TileWorld.Engine.Input;

/// <summary>
/// Defines the engine-level keys currently exposed by the host abstraction.
/// </summary>
public enum InputKey
{
    /// <summary>
    /// The number-row <c>1</c> key.
    /// </summary>
    D1 = 0,
    /// <summary>
    /// The number-row <c>2</c> key.
    /// </summary>
    D2 = 1,
    /// <summary>
    /// The number-row <c>3</c> key.
    /// </summary>
    D3 = 2,
    /// <summary>
    /// The <c>F1</c> function key.
    /// </summary>
    F1 = 3,
    /// <summary>
    /// The <c>F5</c> function key.
    /// </summary>
    F5 = 4,
    /// <summary>
    /// The <c>Enter</c> or return key.
    /// </summary>
    Enter = 5,
    /// <summary>
    /// The <c>Escape</c> key.
    /// </summary>
    Escape = 6,
    /// <summary>
    /// The <c>W</c> key.
    /// </summary>
    W = 7,
    /// <summary>
    /// The <c>A</c> key.
    /// </summary>
    A = 8,
    /// <summary>
    /// The <c>S</c> key.
    /// </summary>
    S = 9,
    /// <summary>
    /// The <c>D</c> key.
    /// </summary>
    D = 10,
    /// <summary>
    /// The up-arrow key.
    /// </summary>
    Up = 11,
    /// <summary>
    /// The down-arrow key.
    /// </summary>
    Down = 12,
    /// <summary>
    /// The left-arrow key.
    /// </summary>
    Left = 13,
    /// <summary>
    /// The right-arrow key.
    /// </summary>
    Right = 14,
    /// <summary>
    /// The left shift key.
    /// </summary>
    LeftShift = 15,
    /// <summary>
    /// The right shift key.
    /// </summary>
    RightShift = 16
}
